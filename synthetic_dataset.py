import pandas as pd
import numpy as np

# Fix the random seed so the results are reproducible
np.random.seed(42)

# Set the number of student records
n = 500

# Create the main dataset as a pandas DataFrame
df = pd.DataFrame({
    # Unique identifier for each student
    "student_id": range(1, n + 1),

    # Random ages between 16 and 29
    "age": np.random.randint(16, 30, n),

    # Randomly assign Male or Female
    "gender": np.random.choice(["Male", "Female"], n),

    # Randomly assign Urban or Rural
    "origin": np.random.choice(["Urban", "Rural"], n),

    # High school average between 2.0 and 5.0, with 2 decimal places
    "high_school_avg": np.round(np.random.uniform(2.0, 5.0, n), 2),

    # Admission score between 0 and 100, with 2 decimal places
    "admission_score": np.round(np.random.uniform(0, 100, n), 2),

    # First semester grade between 0.0 and 5.0, with 2 decimal places
    "first_semester_grade": np.round(np.random.uniform(0.0, 5.0, n), 2),

    # Socioeconomic level from 1 to 5
    "socioeconomic_level": np.random.randint(1, 6, n),

    # Randomly assign scholarship Yes or No
    "scholarship": np.random.choice(["Yes", "No"], n),

    # Randomly assign loan Yes or No
    "loan": np.random.choice(["Yes", "No"], n)
})

# Create a simple dropout risk score based on important variables
# Each condition adds 1 point if the student is at higher risk
prob_dropout = (
    (df["first_semester_grade"] < 2.7).astype(int) +   # Low first semester grade
    (df["high_school_avg"] < 3.0).astype(int) +        # Low high school average
    (df["socioeconomic_level"] <= 2).astype(int)       # Low socioeconomic level
)

# Create the target variable "dropout"
# If the risk score is 2 or more, label the student as "Yes"
# Otherwise, label as "No"
df["dropout"] = np.where(prob_dropout >= 2, "Yes", "No")

# Add some randomness so the dataset is not too perfect
# Select 60 random rows
random_idx = np.random.choice(df.index, size=60, replace=False)

# Replace their dropout values with random Yes/No labels
df.loc[random_idx, "dropout"] = np.random.choice(["Yes", "No"], size=60)

# Define the columns where we will insert missing values
cols_with_nulls = [
    "age", "gender", "origin", "high_school_avg", "admission_score",
    "first_semester_grade", "socioeconomic_level", "scholarship", "loan"
]

# Add null values to each selected column
for col in cols_with_nulls:
    # Choose 5% of the rows randomly
    idx = np.random.choice(df.index, size=int(0.05 * n), replace=False)

    # Insert missing values (NaN) in those positions
    df.loc[idx, col] = np.nan

# Select rows where we will add high outlier values
outlier_high_idx = np.random.choice(df.index, size=8, replace=False)

# Select rows where we will add low outlier values
outlier_low_idx = np.random.choice(df.index, size=8, replace=False)

# Insert a very high outlier in first_semester_grade
# Normal values were between 0 and 5, so 10 is an outlier
df.loc[outlier_high_idx, "first_semester_grade"] = 10.0

# Insert a very low outlier in admission_score
# Normal values were between 0 and 100, so -15 is an outlier
df.loc[outlier_low_idx, "admission_score"] = -15.0

# Select a few rows for extreme age values
age_outlier_idx = np.random.choice(df.index, size=5, replace=False)

# Insert unusual age values such as 5 or 60
df.loc[age_outlier_idx, "age"] = np.random.choice([5, 60], size=5)

# Save the dataset as a CSV file
df.to_csv("synthetic_student_dropout_dataset.csv", index=False)

